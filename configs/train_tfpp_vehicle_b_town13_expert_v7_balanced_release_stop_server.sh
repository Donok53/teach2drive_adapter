# Counterbalance v6's false-go behavior on stop/hazard frames.
export STOP_SPEED_TARGET_THRESHOLD=${STOP_SPEED_TARGET_THRESHOLD:-0.5}
export STOP_SPEED_CEILING_MPS=${STOP_SPEED_CEILING_MPS:-0.5}

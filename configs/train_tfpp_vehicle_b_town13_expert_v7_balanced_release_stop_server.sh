#!/usr/bin/env bash
set -euo pipefail

cd "$(dirname "$0")/.."

# Town13 expert imitation, v7.
# v6 recovered launch/release but overcorrected: stop/hazard frames predicted
# too much speed and the direct longitudinal head often missed required brake.
# This preset fine-tunes from v6 and adds explicit stop speed ceilings plus
# brake true-positive losses, while keeping release_go non-stop supervision.

export DATA_ROOT=${DATA_ROOT:-"$HOME/dataset/byeongjae/datasets/t2d_vehicle_b_front_triplet_town13_target_3h"}
export WORK_ROOT=${WORK_ROOT:-"$HOME/teach2drive/runs/tfpp_vehicle_b_front_triplet_town13_expert_v1"}
export OUT=${OUT:-"$HOME/teach2drive/runs/tfpp_vehicle_b_front_triplet_town13_expert_v7_balanced_release_stop/train_front_triplet_shifted_town13_expert_v7_balanced_release_stop"}
export INIT_CHECKPOINT=${INIT_CHECKPOINT:-"$HOME/teach2drive/runs/tfpp_vehicle_b_front_triplet_town13_expert_v6_release_recovery/train_front_triplet_shifted_town13_expert_v6_release_recovery/best_model.pt"}

export CAMERAS=${CAMERAS:-left,front,right}
export EPOCHS=${EPOCHS:-60}
export EARLY_STOP_PATIENCE=${EARLY_STOP_PATIENCE:-15}
export BATCH_SIZE=${BATCH_SIZE:-128}
export CACHE_BATCH_SIZE=${CACHE_BATCH_SIZE:-128}
